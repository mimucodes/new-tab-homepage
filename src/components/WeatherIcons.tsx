type WeatherIconProps = {
  condition: string
  isDaytime: boolean
}

export const WeatherIcon = (props: WeatherIconProps) => {
  switch (props.condition) {
    case 'Thunderstorm':
      return <Thunderstorm />
    case 'Drizzle':
      return <Rain />
    case 'Rain':
      return <Rain />
    case 'Snow':
      return <Snow />
    case 'Atmosphere':
      return <Atmosphere />
    case 'Clear':
      if (props.isDaytime) return <Sunny />
      return <Clear />
    case 'Clouds':
      return <Clouds />
    default:
      return null
  }
}
